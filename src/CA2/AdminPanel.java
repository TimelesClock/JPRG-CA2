    //Populate all the dropdowns/combo boxes with rentee data

    private void populateRentee() {
        renteeList.removeAllItems();
        renteeCombo.removeAllItems();
        renteeArr = rental.getRenteeArr();
        for (Rentee rentee : renteeArr) {
            renteeList.addItem(rentee.getName());
            renteeCombo.addItem(rentee.getName());
        }
    }

    //Populate all the dropdown/combo boxes with comic data
    private void populateComic() {
        comicList.removeAllItems();
        comicCombo.removeAllItems();
        ArrayList<Comic> comicArr = rental.getComicArr();
        for (Comic comic : comicArr) {
            comicList.addItem(comic.getTitle());
            comicCombo.addItem(comic.getTitle());
        }
    }

