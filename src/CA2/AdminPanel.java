    private void addComicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addComicActionPerformed
        try {
            //Add new comic into the comic array
            String ISBN = isbn.getText();
            String Title = title.getText();
            String Pages = pages.getText();
            String Cost = cost.getText();
            String Language = (String) language.getSelectedItem();
            String Type = (String) type.getSelectedItem();
            ArrayList<Comic> comicArr = rental.getComicArr();
            List<String> ISBNlist = new ArrayList<String>();
            for (Comic comic : comicArr) {
                ISBNlist.add(comic.getISBN());
            }

            if (!rental.validateISBN(ISBN)) {
                showError("The ISBN-13 entered is not valid!");
                return;
            }

            if (ISBNlist.contains(ISBN)) {
                showError("A comic with the same ISBN-13 has already been added!");
                return;
            }

            if (!rental.isInt(Pages)) {
                showError("Pages must be an integer!");
                return;
            }

            if (!rental.isNum(Cost)) {
                showError("Cost must be a double value!");
                return;
            }
            //Above is validation
            rental.addToComic(Title, ISBN, Pages, Cost, Type, Language);
            JOptionPane.showMessageDialog(null, "Comic Added Succesfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            populateComic();

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_addComicActionPerformed

